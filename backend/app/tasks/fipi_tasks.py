@celery_app.task(bind=True, name="sync_subject_full")
def sync_subject_full(self, subject_name="История", exam_type="EGE"):
    """Full sync for all leaf themes of a subject. Reports per-theme progress."""
    from app.models import Theme

    codifier = get_codifier(subject_name, exam_type)
    db = _get_sync_session()
    try:
        subject = db.query(Theme).filter(Theme.fipi_code == list(codifier.keys())[0]).first()
        if not subject:
            return {"error": f"Subject {subject_name} not found"}

        # Only sync leaf themes (themes without child themes in the codifier)
        all_theme_codes = set(codifier.keys())
        parent_codes = set()
        for code in all_theme_codes:
            # A code is a parent if there exist other codes that start with it + "."
            for other_code in all_theme_codes:
                if other_code != code and other_code.startswith(code + "."):
                    parent_codes.add(code)
                    break
        leaf_codes = all_theme_codes - parent_codes

        themes = db.query(Theme).filter(Theme.subject_id == subject.subject_id).all()
        # Filter to leaf themes only
        theme_codes = [t.fipi_code for t in themes if t.fipi_code in leaf_codes]
    finally:
        db.close()

    results = {}
    for i, code in enumerate(theme_codes):
        self.update_state(state="PROGRESS", meta={
            "current": i + 1,
            "total": len(theme_codes),
            "theme": code,
        })
        result = _sync_theme_core(code, exam_type=exam_type, subject_name=subject_name)
        results[code] = result

    total_added = sum(r.get("added_new", 0) for r in results.values() if isinstance(r, dict))
    total_fetched = sum(r.get("fetched_from_fipi", 0) for r in results.values() if isinstance(r, dict))

    # After all themes synced, run image sync from full list once
    # (FIPI blocks images when theme=X. is used, so full list is required)
    self.update_state(state="PROGRESS", meta={
        "status": "Синхронизация изображений из полного списка...",
    })
    try:
        img_result = _sync_images_from_full_list(max_pages=200, exam_type=exam_type, subject_name=subject_name)
    except Exception as e:
        img_result = {"error": str(e)}

    return {
        "subject": subject_name,
        "exam_type": exam_type,
        "themes_processed": len(theme_codes),
        "total_fetched": total_fetched,
        "total_added": total_added,
        "images_sync": img_result,
        "per_theme": results,
    }


@celery_app.task(bind=True, name="create_test_from_fipi")
def create_test_from_fipi(self, tutor_id, title, theme_codes, count_per_theme, task_type, time_limit_minutes=None, exam_positions=None):
    """Create a test from LOCAL database only. No live FIPI requests."""
    from app.models import Theme, Task, Test, TestTask
    from uuid import UUID
    import random

    db = _get_sync_session()
    try:
        selected_task_ids = []
        theme_stats = {}
        warnings = []

        for theme_code in theme_codes:
            theme = db.query(Theme).filter(Theme.fipi_code == theme_code).first()
            if not theme:
                warnings.append(f"Тема {theme_code} не найдена в базе")
                theme_stats[theme_code] = 0
                continue

            query = db.query(Task).filter(Task.theme_id == theme.id)
            if task_type and task_type != "MIX":
                query = query.filter(Task.type == task_type)

            # Filter by exam positions (KIM types)
            if exam_positions:
                query = query.filter(Task.exam_position.in_(exam_positions))

            available = query.all()
            available_count = len(available)

            if available_count == 0:
                warnings.append(f"Тема {theme_code}: нет заданий в базе (нужна синхронизация)")
                theme_stats[theme_code] = 0
                continue

            take = min(count_per_theme, available_count)
            if take < count_per_theme:
                warnings.append(
                    f"Тема {theme_code}: доступно {available_count} заданий, "
                    f"запрошено {count_per_theme} (взято {take})"
                )

            chosen = random.sample(available, take)
            for t in chosen:
                selected_task_ids.append(str(t.id))
            theme_stats[theme_code] = take

        self.update_state(state="PROGRESS", meta={
            "stage": "creating",
            "status": f"Собрано {len(selected_task_ids)} заданий из локальной базы...",
            "tasks_found": len(selected_task_ids),
            "theme_stats": theme_stats,
        })

        if not selected_task_ids:
            return {
                "error": "Нет заданий в локальной базе. Запустите синхронизацию с ФИПИ.",
                "warnings": warnings,
            }

        test = Test(tutor_id=UUID(tutor_id), title=title, time_limit_minutes=time_limit_minutes)
        db.add(test)
        db.flush()

        random.shuffle(selected_task_ids)
        for i, task_id in enumerate(selected_task_ids):
            db.add(TestTask(test_id=test.id, task_id=UUID(task_id), order_number=i + 1))

        db.commit()
        db.refresh(test)

        result = {
            "test_id": str(test.id),
            "title": test.title,
            "tasks_count": len(selected_task_ids),
            "theme_stats": theme_stats,
        }
        if warnings:
            result["warnings"] = warnings
        return result
    finally:
        db.close()


def _sync_images_from_full_list(max_pages=200, exam_type='EGE', subject_name='История'):
    """Sync images by iterating through FIPI full list (no theme filter).

    Matching strategy (two-tier):
    1. GUID — precise match via metadata_.fipi_guid (handles duplicate texts
       across different FIPI pages, each with its own context image)
    2. Text hash — fallback for tasks without GUID

    FIPI blocks ShowPicture/ShowPictureQ when theme=X. is used.
    This function fetches the full list (pagesize=10, no theme filter)
    to get images, then matches to existing tasks in DB.

    Returns: {pages_scanned, tasks_with_images, images_downloaded, updated}
    """
    import time
    import hashlib
    import json
    from app.models import Task, Subject
    from app.services.image_downloader import download_task_images

    def compute_text_hash(text_content: dict) -> str:
        """Compute hash of text_content for matching."""
        normalized = json.dumps(text_content, sort_keys=True, ensure_ascii=False)
        return hashlib.sha256(normalized.encode()).hexdigest()

    base_url = get_base_url(exam_type)
    project_id = get_project_id(exam_type, subject_name)

    db = _get_sync_session()
    try:
        # Find subject to filter tasks by subject_id (ISOLATION per bank)
        subject = db.query(Subject).filter(
            Subject.name == subject_name, Subject.exam_type == exam_type
        ).first()
        if not subject:
            return {"error": f"Subject {subject_name} ({exam_type}) not found"}

        # Build TWO mappings: GUID → task AND text_hash → task
        # CRITICAL: filter by subject_id to avoid cross-bank contamination
        all_tasks = db.query(Task).filter(
            Task.text_content.isnot(None),
            Task.subject_id == subject.id,
        ).all()

        guid_to_task = {}
        text_hash_to_task = {}
        for t in all_tasks:
            meta = t.metadata_ or {}
            guid = meta.get("fipi_guid")
            if guid:
                guid_to_task[guid.upper()] = t

            tc = t.text_content or {}
            if tc.get("text"):
                text_hash = compute_text_hash({"text": tc["text"]})
                # Only store in text_hash if no GUID mapping exists (don't overwrite)
                if text_hash not in text_hash_to_task:
                    text_hash_to_task[text_hash] = t

        logger.info("Found %d tasks with GUIDs, %d with text hashes",
                     len(guid_to_task), len(text_hash_to_task))

        pages_scanned = 0
        tasks_with_images = 0
        images_downloaded = 0
        updated = 0
        no_match = 0
        guid_matched = 0
        hash_matched = 0

        with httpx.Client(timeout=30, follow_redirects=True, verify=False) as client:
            page = 1
            consecutive_empty = 0

            while page <= max_pages:
                resp = client.post(
                    f"{base_url}/questions.php",
                    data={
                        "search": "1",
                        "pagesize": "10",
                        "proj": project_id,
                        # NO theme parameter — this is the key difference
                        "page": str(page),
                    },
                    headers=HEADERS,
                )
                html = resp.content.decode("windows-1251", errors="replace")
                page_tasks = _extract_tasks_from_html(html)

                if not page_tasks:
                    consecutive_empty += 1
                    if consecutive_empty >= 3:
                        break  # 3 empty pages in a row = end of list
                    page += 1
                    time.sleep(0.5)
                    continue

                consecutive_empty = 0
                pages_scanned += 1

                for task_data in page_tasks:
                    images = [i for i in task_data.get("images", []) if i]
                    if not images:
                        continue

                    tasks_with_images += 1

                    # TIER 1: Match by GUID (precise, handles duplicate texts)
                    task_guid = task_data.get("guid", "").upper()
                    db_task = None
                    if task_guid:
                        db_task = guid_to_task.get(task_guid)
                        if db_task:
                            guid_matched += 1

                    # TIER 2: Fallback to text hash (for tasks without GUID in DB)
                    if not db_task:
                        cell_text = task_data.get("cell_text", "") or task_data.get("text", "")
                        if not cell_text:
                            continue
                        text_hash = compute_text_hash({"text": cell_text})
                        db_task = text_hash_to_task.get(text_hash)
                        if db_task:
                            hash_matched += 1

                    if not db_task:
                        no_match += 1
                        continue

                    # Compare FIPI URLs with stored URLs — update only if different
                    existing_fipi_urls = (db_task.text_content or {}).get("fipi_urls", [])
                    new_fipi_urls = [p for p in images if p]

                    # Skip if same FIPI URLs already stored
                    if existing_fipi_urls == new_fipi_urls:
                        continue

                    # Download images (skip files already on disk)
                    local_paths = download_task_images(images)

                    # Update text_content — always store fipi_urls, only update images if download succeeded
                    text_content = dict(db_task.text_content or {})
                    text_content["fipi_urls"] = new_fipi_urls
                    if local_paths and any(local_paths):
                        text_content["images"] = local_paths
                        images_downloaded += sum(1 for p in local_paths if p)
                    db_task.text_content = text_content
                    updated += 1

                if pages_scanned % 20 == 0:
                    logger.info(
                        "Image sync: scanned %d pages, updated %d tasks "
                        "(%d GUID, %d hash, %d no match)",
                        pages_scanned, updated, guid_matched, hash_matched, no_match
                    )

                page += 1
                time.sleep(0.5)  # polite delay

        db.commit()

        result = {
            "pages_scanned": pages_scanned,
            "tasks_with_images": tasks_with_images,
            "images_downloaded": images_downloaded,
            "updated": updated,
            "guid_matched": guid_matched,
            "hash_matched": hash_matched,
            "no_match": no_match,
        }
        logger.info("Image sync complete: %s", result)
        return result

    finally:
        db.close()


