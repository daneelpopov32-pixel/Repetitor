def _sync_tasks_from_full_list(max_pages=200, exam_type='EGE', subject_name='История'):
    """Sync tasks by iterating through FIPI full list (no theme filter).

    Fetches tasks from FIPI without a theme filter, matches them to existing
    themes in DB by theme code from КЭС metadata, and saves new tasks.
    This is useful for bulk-syncing tasks when per-theme fetch is blocked.

    Returns: {pages_scanned, tasks_found, added_new, skipped_duplicates, per_theme}
    """
    import time
    from app.models import Task, Theme
    from app.services.content_parser import compute_text_hash
    from app.services.kim_mapping import classify_task
    from app.services.image_downloader import download_task_images

    base_url = get_base_url(exam_type)
    project_id = get_project_id(exam_type, subject_name)

    db = _get_sync_session()
    try:
        # Find subject to filter tasks by subject_id (ISOLATION per bank)
        from app.models import Subject
        subject = db.query(Subject).filter(
            Subject.name == subject_name, Subject.exam_type == exam_type
        ).first()
        if not subject:
            return {"error": f"Subject {subject_name} ({exam_type}) not found"}

        # Build theme lookup: fipi_code → Theme (filtered by subject)
        themes = db.query(Theme).filter(Theme.subject_id == subject.id).all()
        code_to_theme = {t.fipi_code: t for t in themes}

        # Pre-load existing GUIDs for dedup within this subject only
        existing_guids = set()
        for row in db.query(Task.metadata_["fipi_guid"].as_string()).filter(
            Task.metadata_["fipi_guid"].isnot(None),
            Task.subject_id == subject.id,
        ).all():
            if row[0]:
                existing_guids.add(row[0].upper())

        pages_scanned = 0
        tasks_found = 0
        added_new = 0
        skipped = 0
        per_theme = {}

        with httpx.Client(timeout=30, follow_redirects=True, verify=False) as client:
            page = 1
            consecutive_empty = 0

            while page <= max_pages:
                resp = client.post(
                    f"{base_url}/questions.php",
                    data={
                        "search": "1",
                        "pagesize": "10",
                        "proj": project_id,
                        "page": str(page),
                    },
                    headers=HEADERS,
                )
                html = resp.content.decode("windows-1251", errors="replace")
                page_tasks = _extract_tasks_from_html(html)

                if not page_tasks:
                    consecutive_empty += 1
                    if consecutive_empty >= 3:
                        break
                    page += 1
                    time.sleep(0.5)
                    continue

                consecutive_empty = 0
                pages_scanned += 1

                for task_data in page_tasks:
                    tasks_found += 1

                    # Global dedup by GUID
                    task_guid = task_data.get("guid", "")
                    if task_guid and task_guid.upper() in existing_guids:
                        skipped += 1
                        continue

                    # Find matching theme by looking at КЭС metadata in the task
                    # The theme code is embedded in the task's HTML metadata
                    # We try to match it to an existing theme
                    task_theme = None
                    # Try to extract theme code from task metadata or text
                    for code in code_to_theme:
                        if code in str(task_data.get("text", "")):
                            task_theme = code_to_theme[code]
                            break

                    if not task_theme:
                        # No matching theme found — skip or use first theme
                        skipped += 1
                        continue

                    text_content = _build_text_content(task_data)

                    # Download images
                    raw_images = text_content.get("images", [])
                    if raw_images:
                        local_paths = download_task_images(raw_images)
                        text_content["images"] = local_paths

                    text_hash = compute_text_hash(text_content)
                    subtype = task_data.get("subtype", "")
                    text = task_data.get("text", "")
                    exam_position, difficulty_level = classify_task(subtype, text, text_content)

                    task = Task(
                        subject_id=task_theme.subject_id,
                        theme_id=task_theme.id,
                        type=task_data["type"],
                        text_content=text_content,
                        correct_answer_key=None,
                        fipi_criteria=None,
                        source_url=f"{base_url}/questions.php?proj={project_id}&theme={task_theme.fipi_code}",
                        metadata_={
                            "text_hash": text_hash,
                            "fipi_guid": task_guid,
                            "subtype": subtype,
                        },
                        exam_position=exam_position,
                        difficulty_level=difficulty_level,
                    )
                    db.add(task)
                    if task_guid:
                        existing_guids.add(task_guid.upper())
                    added_new += 1
                    per_theme.setdefault(task_theme.fipi_code, 0)
                    per_theme[task_theme.fipi_code] += 1

                if pages_scanned % 20 == 0:
                    logger.info(
                        "Task sync from full list: scanned %d pages, found %d, added %d",
                        pages_scanned, tasks_found, added_new
                    )

                page += 1
                time.sleep(0.5)

        db.commit()

        result = {
            "pages_scanned": pages_scanned,
            "tasks_found": tasks_found,
            "added_new": added_new,
            "skipped_duplicates": skipped,
            "per_theme": per_theme,
        }
        logger.info("Task sync from full list complete: %s", result)
        return result

    finally:
        db.close()


